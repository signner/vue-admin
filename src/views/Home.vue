<script>
    import $ from 'jquery/dist/jquery.min'

    export default {
        data() {
            return {
                sysName: '后台管理',
                collapsed: false,
                sysUserName: '',
                sysUserAvatar: '',
                form: {
                    name: '',
                    region: '',
                    date1: '',
                    date2: '',
                    delivery: false,
                    type: [],
                    resource: '',
                    desc: ''
                }
            }
        },
        methods: {
            onSubmit() {
                console.log('submit!');
            },
            //退出登录
            logout: function () {
                var _this = this;
                this.$confirm('确认退出吗?', '提示', {
                    //type: 'warning'
                }).then(() => {
                    sessionStorage.removeItem('user');
                    _this.$router.push('/login');
                }).catch(() => {

                });


            },
            //折叠导航栏
            collapse: function () {
                this.collapsed = !this.collapsed;
                this.toggle(this.collapsed)
            },
            showMenu(i, status) {
                //ref="menuCollapsed"
                this.$refs.menuCollapsed.getElementsByClassName('submenu-hook-' + i)[0].style.display = status ? 'block' : 'none';
            },
            toggle: function (collapsed) {
                if (collapsed) {
                    $("#el").stop().animate({'width': '60px'}, 200).hide();
                    $("#log").stop().animate({'width': '60px'}, 200)
                    $("#ul").stop().animate({'width': '60px'}, 200).show();
                } else {
                    $("#ul").stop().animate({'width': '230px'}, 200).hide();
                    $("#log").stop().animate({'width': '230px'}, 200)
                    $("#el").stop().animate({'width': '230px'}, 200).show();
                }
                // console.log(collapsed)
            }
        },
        mounted() {
            var user = sessionStorage.getItem('user');
            if (user) {
                user = JSON.parse(user);
                this.sysUserName = user.name || '';
                this.sysUserAvatar = user.avatar || '';
            }
            // console.log(this.$router, this.$route);
        }
    }

</script>

